import java.lang.*;
class Datatypes
{
	public static void main(String[] args)
	{
		int a;
		float b;
		char c;
		double d;
		long g;
		System.out.println("Size of int is "+(Integer.SIZE/8)+" and no default value");
		System.out.println("Size of long is "+(Long.SIZE/8)+" and no default value ");
		System.out.println("Size of float is "+(Float.SIZE/8)+" and no default value ");
		System.out.println("Size of double is "+(Double.SIZE/8)+" and no default value  ");
		System.out.println("Size of char is "+(Character.SIZE/8)+" and no default value ");
	}
}